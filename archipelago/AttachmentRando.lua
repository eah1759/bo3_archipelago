    end
end

function load_reticles_into_gsc(reticle_data)
    for weapon_name, reticle in pairs(reticle_data) do
        Engine.SetDvar("ARCHIPELAGO_RETICLE_RANDO_" .. string.upper(weapon_name), reticle)
    end
end

