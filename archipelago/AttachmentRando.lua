function generate_weapon_camos_for_seed(seed, base_enabled, base_mixed, pap_enabled, pap_mixed, joined)
    local numeric_seed = 0
    for i = 1, #seed do
        -- Multi by pos so abc doesn't equal cba
        numeric_seed = numeric_seed + (string.byte(seed, i) * i)
    end
    math.randomseed(numeric_seed)

    local function shuffle_array(arr)
        local shuffled = {}
        for i, v in ipairs(arr) do
            shuffled[i] = v
        end
        for i = #shuffled, 2, -1 do
            local j = math.random(i)
            shuffled[i], shuffled[j] = shuffled[j], shuffled[i]
        end
        return shuffled
    end

    local camo_data = {}

    if base_enabled ~= 0 then
        -- If joined, assume mixed
        local use_mixed = (joined ~= 0) or (base_mixed ~= 0)
        
        local camo_bag
        if use_mixed then
            -- Combine both camo tables
            local combined_camos = {}
            for _, camo in ipairs(camo_table) do
                table.insert(combined_camos, camo)
            end
            for _, camo in ipairs(pap_camo_table) do
                table.insert(combined_camos, camo)
            end
            camo_bag = shuffle_array(combined_camos)
        else
            camo_bag = shuffle_array(camo_table)
        end

        local bag_index = 1
        for _, weapon_data in ipairs(weapon_attachments) do
            local weapon_name = weapon_data.name
            local camo_roll = camo_bag[bag_index]
            
            camo_data[weapon_name] = camo_roll.models.camoIndex
            
            -- If joined and pap enabled, assign same camo to pap'd weapon
            if joined ~= 0 and pap_enabled ~= 0 then
                camo_data[weapon_name .. "_upgraded"] = camo_roll.models.camoIndex
            end
            
            bag_index = bag_index + 1
            if bag_index > #camo_bag then
                bag_index = 1
            end
        end
    end

    if pap_enabled ~= 0 and (joined == 0 or base_enabled == 0) then
        local camo_bag
        if pap_mixed ~= 0 then
            -- Combine both camo tables
            local combined_camos = {}
            for _, camo in ipairs(camo_table) do
                table.insert(combined_camos, camo)
            end
            for _, camo in ipairs(pap_camo_table) do
                table.insert(combined_camos, camo)
            end
            camo_bag = shuffle_array(combined_camos)
        else
            camo_bag = shuffle_array(pap_camo_table)
        end

        local bag_index = 1
        for _, weapon_data in ipairs(weapon_attachments) do
            local weapon_name = weapon_data.name .. "_upgraded"
            local camo_roll = camo_bag[bag_index]
            
            camo_data[weapon_name] = camo_roll.models.camoIndex
            
            bag_index = bag_index + 1
            if bag_index > #camo_bag then
                bag_index = 1
            end
        end
    end

    return camo_data
end

